#Write a program to perform different Arithmetic Operations on numbers in Python

num1 = 15
num2 = 5

#Concatenation Operator - '+'
print("Concatenation Operator: ",num1 + num2)

#Substraction Operator - '-'
print("Substraction Operator: ",num1 - num2)

#Multiplication Operator - '*'
print("Multiplication Operator: ",num1 * num2)

#Division Operator - '/'
print("Division Operator: ",num1 / num2)

#Floor Division Operator - '//'
print("Floor Division Operator: ",num1 // num2)

#Modulo Operator - '%'
print ("Modulo Operator: ", num1 % num2)  

#Exponential Operator - '**'
print ('Expontial Operator: ', num1 ** num2)  